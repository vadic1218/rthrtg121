def parse_schedules(raw_text: str) -> dict[str, dict[str, list[str]]]:
    result: dict[str, dict[str, list[str]]] = {}
    current_class: str | None = None
    current_day: str | None = None

    for raw_line in raw_text.splitlines():
        line = raw_line.strip()
        if not line:
            continue
        if CLASS_RE.fullmatch(line):
            current_class = line
            current_day = None
            result[current_class] = {}
            continue
        if line in DAYS:
            if current_class is None:
                continue
            current_day = line
            result[current_class][current_day] = []
            continue
        if current_class and current_day:
            result[current_class][current_day].append(line)

    return result


def _repair_text(value: str) -> str:
    try:
        return value.encode("latin1").decode("utf-8")
    except (UnicodeEncodeError, UnicodeDecodeError):
        return value


DAYS = [_repair_text(day) for day in DAYS]
RAW_SCHEDULES = _repair_text(RAW_SCHEDULES)

CLASS_LETTER_MAP = {"?": "?", "?": "?", "?": "?", "?": "?", "?": "?", "?": "?", "?": "?"}


def _normalize_class_name(class_name: str) -> str:
    if not class_name:
        return class_name
    return class_name[:-1] + CLASS_LETTER_MAP.get(class_name[-1], class_name[-1])


SCHEDULES = {
    _normalize_class_name(class_name): days
    for class_name, days in parse_schedules(RAW_SCHEDULES).items()
}


def letters_for_grade(grade: str) -> list[str]:
    grade = str(grade)
    letters: list[str] = []
    pattern = re.compile(r"^(\d{1,2})([\u0410-\u042f\u0401])$")
    for class_name in SCHEDULES:
        match = pattern.match(class_name)
        if not match:
            continue
        class_grade, class_letter = match.groups()
        if class_grade == grade:
            letters.append(class_letter)
    order = [chr(code) for code in (0x0410, 0x0411, 0x0412, 0x0413, 0x0414, 0x0415, 0x0416, 0x0417, 0x0418, 0x041A, 0x041B, 0x041C, 0x041D, 0x041E, 0x041F)]
    index_map = {letter: idx for idx, letter in enumerate(order)}
    return sorted(set(letters), key=lambda item: index_map.get(item, 999))
